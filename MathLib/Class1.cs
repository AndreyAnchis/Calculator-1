using System;

namespace MathLib
{
    public class Class1
    {
        public static double SCR(double a, double b) {
            a = Math.Pow(a,b);
            return a;
        }
        public static double SIN(double a)
        {
            a = Math.Sin(a);
            return a;
        }
        public static double MOD(double a)
        {
            a = Math.Abs(a);
            return a;
        }


    }
}
